class TrainerModel {
  final String initials;
  final String name;
  final String specialty;
  final int experience;
  final int clients;
  final double rating;
  final String email;
  final String phone;

  TrainerModel({
    required this.initials,
    required this.name,
    required this.specialty,
    required this.experience,
    required this.clients,
    required this.rating,
    required this.email,
    required this.phone,
  });
}
